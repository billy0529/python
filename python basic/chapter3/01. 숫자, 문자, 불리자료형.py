# - 숫자 자려향
# 1. 정수형 : 소숫점이 없는 수
print(1,3,0,-1, sep="") # 마지막 sep=""은 인수끼리 스페이스 공백이 없게 한다.
# 2. 실수형 : 소숫점이 있는 수
print(1.1,3.1,0,-1.4)

# - 문자열 자료형
print("파이썬 너무 재밌다", end='') # end는 다음 print문과 스페이스 없이 이어서 
print('파이썬 정말 재밌자너!')

# - 불린형 자료형
# true or false
print(True)
print(False)